package main

import "fmt"

func main() {
	var name string

	name = "Sulkifli"
	fmt.Println(name)

	name = "sul"
	fmt.Println(name)

	var friendName = "Budi"
	fmt.Println(friendName)

	var age = 25
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	var (
		firstName = "Sulkifli"
		lastName  = "Sul"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
